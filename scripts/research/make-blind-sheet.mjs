// Build a blind rating sheet for the human anchor of the rewrite-efficacy study
// (pre-registration control #5: judge<->human concordance sets the trust ceiling
// on the LLM judges; also the pilot for issue #159).
//
// The rater sees passages in a deterministic shuffled order with condition labels
// stripped. The answer key is written to a SEPARATE file that the rater must not
// open until the sheet is filled in.
//
// Usage: node scripts/research/make-blind-sheet.mjs [--lang ko] [--n 15]

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
const DIR = join(ROOT, 'artifacts', 'rewrite-efficacy-pilot');

const args = process.argv.slice(2);
const argOf = (name, dflt) => {
  const i = args.indexOf(`--${name}`);
  return i >= 0 && args[i + 1] ? args[i + 1] : dflt;
};
const LANG = argOf('lang', 'ko');
const N = Number(argOf('n', 15));

/** Seeded shuffle so the sheet and its key always agree and the run reproduces. */
function lcg(seed = 20260710) {
  let s = seed >>> 0;
  return () => { s = (Math.imul(s, 1664525) + 1013904223) >>> 0; return s / 4294967296; };
}

function loadTexts() {
  const rows = [];
  for (const f of readdirSync(DIR).filter((x) => /^texts.*\.private\.jsonl$/.test(x))) {
    for (const line of readFileSync(join(DIR, f), 'utf8').split('\n')) {
      if (!line.trim()) continue;
      try { rows.push(JSON.parse(line)); } catch { /* partial line */ }
    }
  }
  return rows;
}

function main() {
  const rows = loadTexts().filter((r) => r.lang === LANG);
  if (!rows.length) { console.error(`no texts for lang=${LANG} in ${DIR}`); process.exit(1); }

  // Three conditions per the pre-registered 3-way blind: original AI, its
  // rewrite, and a real human control.
  //
  // CRITICAL: never show a passage AND its own rewrite in the same sheet. They
  // share topic, facts, and often whole clauses, so seeing both lets the rater
  // infer the pairing and breaks the blind. Each source item contributes exactly
  // ONE condition, assigned by the seeded RNG.
  const rnd = lcg();
  const items = [];
  for (const r of rows) {
    if (r.source_class === 'human' && r.original) {
      items.push({ condition: 'human-original', text: r.original, sha: r.original_sha });
      continue;
    }
    if (r.source_class !== 'ai') continue;
    const canOriginal = Boolean(r.original);
    const canRewrite = Boolean(r.rewritten);
    if (canOriginal && canRewrite) {
      const useRewrite = rnd() < 0.5;
      items.push(useRewrite
        ? { condition: 'ai-rewritten', text: r.rewritten, sha: r.rewrite_sha }
        : { condition: 'ai-original', text: r.original, sha: r.original_sha });
    } else if (canOriginal) {
      items.push({ condition: 'ai-original', text: r.original, sha: r.original_sha });
    } else if (canRewrite) {
      items.push({ condition: 'ai-rewritten', text: r.rewritten, sha: r.rewrite_sha });
    }
  }

  for (let i = items.length - 1; i > 0; i -= 1) {
    const j = Math.floor(rnd() * (i + 1));
    [items[i], items[j]] = [items[j], items[i]];
  }
  const picked = items.slice(0, Math.min(N, items.length));

  const counts = picked.reduce((m, p) => { m[p.condition] = (m[p.condition] ?? 0) + 1; return m; }, {});
  const shas = new Set(picked.map((p) => p.sha));
  if (shas.size !== picked.length) { console.error('FATAL: duplicate passage in sheet'); process.exit(1); }

  const sheet = [];
  sheet.push(`# Blind rating sheet — ${LANG} (${picked.length} passages)`);
  sheet.push('');
  sheet.push('For each passage, without looking anything up:');
  sheet.push('');
  sheet.push('1. **Authorship**: was this written by a person, or generated by AI? (`human` / `ai`)');
  sheet.push('2. **AI-likeness**: 0-100. 0 = unmistakably an individual person wrote it; 100 = unmistakably generic AI output. Judge the PROSE (rhythm, word choice, structure, specificity), not the topic.');
  sheet.push('3. **Strongest cue**: the one thing that drove your call.');
  sheet.push('');
  sheet.push('Passages are shuffled and unlabelled. Some are AI text, some are AI text rewritten by patina, some are human-written. Do not open `blind-sheet-KEY.md` until you are done.');
  sheet.push('');
  sheet.push('---');
  sheet.push('');
  picked.forEach((item, i) => {
    sheet.push(`## Passage ${i + 1}`);
    sheet.push('');
    sheet.push('```');
    sheet.push(item.text);
    sheet.push('```');
    sheet.push('');
    sheet.push('- authorship: ');
    sheet.push('- ai_likeness: ');
    sheet.push('- strongest_cue: ');
    sheet.push('');
  });

  const key = ['# Answer key — DO NOT OPEN BEFORE RATING', ''];
  key.push('| passage | condition | sha |');
  key.push('|---:|---|---|');
  picked.forEach((item, i) => key.push(`| ${i + 1} | ${item.condition} | ${item.sha} |`));

  const sheetPath = join(DIR, `blind-sheet-${LANG}.md`);
  const keyPath = join(DIR, `blind-sheet-KEY-${LANG}.md`);
  writeFileSync(sheetPath, sheet.join('\n'));
  writeFileSync(keyPath, key.join('\n'));
  const breakdown = Object.entries(counts).map(([k, v]) => `${k}=${v}`).join(' ');
  console.log(`sheet: ${sheetPath}\nkey:   ${keyPath}\npassages: ${picked.length}  [${breakdown}]`);
  if (!counts['human-original']) console.log('WARN: no human controls in this sheet — the 3-way blind is incomplete.');
}

main();
