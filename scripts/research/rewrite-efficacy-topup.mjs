// Top-up pass for the rewrite-efficacy pilot.
//
// Transient backend failures (a gemini judge call hanging past its timeout on
// both attempts, a one-off `claude exited with code 1`) leave holes in the data.
// Dropping the affected rows would be a silent, NON-RANDOM deletion: the holes
// land on whatever the backend choked on. Krippendorff's alpha also needs BOTH
// judges on a passage, so one missing rating destroys a whole agreement unit.
//
// This re-runs ONLY the failed calls, against the stored text, with the same
// prompt, the same parser, and the same models. Nothing about the measurement
// changes — only the number of attempts. Every repaired cell is marked
// `topped_up: true` so the results can report how much of the data needed it.
//
// Run it with nothing else competing for the local CLIs; concurrent invocations
// are what caused most of the hangs in the first place.
//
// Usage: node scripts/research/rewrite-efficacy-topup.mjs [--dry-run]

import { spawn } from 'node:child_process';
import { createHash } from 'node:crypto';
import { readdirSync, readFileSync, writeFileSync, appendFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
const DIR = join(ROOT, 'artifacts', 'rewrite-efficacy-pilot');
const LOG = join(DIR, 'topup.log');
const DRY = process.argv.includes('--dry-run');

const REWRITER = 'claude-cli';
// Primary panel after Deviation 3 (codex quota exhausted mid-pilot). Any
// judge-gpt cells already collected are LEFT ALONE: they are a partial third
// rater, reported separately, never merged into the primary panel.
const KIMI_ARGS = ['--print', '--input-format', 'text', '--output-format', 'text',
  '--final-message-only', '--no-thinking', '--max-steps-per-turn', '20'];
const JUDGES = [
  { id: 'judge-gemini', cmd: 'gemini', args: [] },
  { id: 'judge-kimi', cmd: 'kimi', args: KIMI_ARGS },
];
// Generous: the top-up runs alone, and a slow answer still beats a missing one.
const JUDGE_TIMEOUT_MS = 600_000;
const REWRITE_TIMEOUT_MS = 420_000;
const SCORE_KEYS = ['ai_likeness', 'ai_status', 'ai_score', 'score', 'aiLikeness'];

const sha = (s) => createHash('sha256').update(s, 'utf8').digest('hex').slice(0, 16);
const log = (m) => {
  const line = `[${new Date().toISOString()}] ${m}`;
  console.log(line);
  if (!DRY) appendFileSync(LOG, line + '\n');
};

function run(cmd, args, { input = '', timeout = 120_000 } = {}) {
  return new Promise((resolve) => {
    const child = spawn(cmd, args, { cwd: ROOT, stdio: ['pipe', 'pipe', 'pipe'], detached: true });
    let stdout = '';
    let stderr = '';
    let settled = false;
    const finish = (v) => { if (!settled) { settled = true; clearTimeout(timer); resolve(v); } };
    const timer = setTimeout(() => {
      // Kill the whole process group: the local CLIs spawn helpers that survive a
      // bare child.kill() and then linger as orphans, blocking the next attempt.
      try { process.kill(-child.pid, 'SIGKILL'); } catch { try { child.kill('SIGKILL'); } catch { /* noop */ } }
      finish({ ok: false, stdout, stderr, error: `timeout after ${timeout}ms` });
    }, timeout);
    child.stdout.on('data', (d) => { stdout += d; });
    child.stderr.on('data', (d) => { stderr += d; });
    child.on('error', (e) => finish({ ok: false, stdout, stderr, error: String(e?.message ?? e) }));
    child.on('close', (code) => finish({ ok: code === 0, code, stdout, stderr }));
    if (input) child.stdin.write(input);
    child.stdin.end();
  });
}

function judgePrompt(text, lang) {
  return [
    'You are evaluating a single passage of writing. You do not know its origin.',
    '',
    'Answer BOTH questions independently. Do not let one answer drive the other.',
    '',
    'Q1 (authorship): Was this passage most likely written by a human, or generated by an AI language model?',
    'Q2 (AI-likeness): On a 0-100 scale, how AI-like does the PROSE read?',
    '   0 = reads unmistakably like an individual human wrote it',
    '   100 = reads unmistakably like generic AI output',
    '   Judge the writing itself (rhythm, word choice, structure, specificity), not the topic.',
    '',
    'Then name the single strongest cue that drove your judgement.',
    '',
    `The passage is in ${lang === 'ko' ? 'Korean' : 'English'}.`,
    '',
    'Respond with ONLY a JSON object, no code fence, no prose:',
    '{"authorship":"human"|"ai","ai_likeness":<0-100 integer>,"strongest_cue":"<short phrase>"}',
    '',
    '--- PASSAGE START ---',
    text,
    '--- PASSAGE END ---',
  ].join('\n');
}

function parseJudge(raw) {
  if (!raw) return null;
  const matches = String(raw).match(/\{[^{}]*"authorship"[^{}]*\}/g);
  if (!matches) return null;
  for (const candidate of [...matches].reverse()) {
    try {
      const o = JSON.parse(candidate);
      const authorship = String(o.authorship || '').toLowerCase();
      if (authorship !== 'human' && authorship !== 'ai') continue;
      const key = SCORE_KEYS.find((k) => Number.isFinite(Number(o[k])));
      if (!key) continue;
      return {
        authorship,
        ai_likeness: Math.max(0, Math.min(100, Math.round(Number(o[key])))),
        strongest_cue: String(o.strongest_cue || '').slice(0, 200),
        score_key: key === 'ai_likeness' ? undefined : key,
      };
    } catch { /* next */ }
  }
  return null;
}

async function patinaRewrite(text, lang) {
  const res = await run('node', [
    'bin/patina.js', '--lang', lang, '--backend', REWRITER, '--format', 'text', '--no-interactive',
  ], { input: text, timeout: REWRITE_TIMEOUT_MS });
  const gateFailed = res.code === 4;
  if (!res.ok && !gateFailed) return { text: null, error: res.error || res.stderr.slice(0, 300) || `exit ${res.code}` };
  const body = String(res.stdout).split(/\n---\n/)[0].trim();
  if (!body) return { text: null, error: 'empty rewrite' };
  const m = String(res.stderr).match(/persona safety gate failed:\s*([^\n]*)/);
  return { text: body, error: null, gate_failed: gateFailed, gate_reason: gateFailed ? (m ? m[1].trim().slice(0, 200) : 'unspecified') : null };
}

function readJsonl(path) {
  return readFileSync(path, 'utf8').split('\n').filter((l) => l.trim())
    .map((l) => { try { return JSON.parse(l); } catch { return null; } }).filter(Boolean);
}

const missing = (cell) => !cell || typeof cell.ai_likeness !== 'number';

async function main() {
  const shards = readdirSync(DIR).filter((f) => /^pilot-rows-.*\.jsonl$/.test(f));
  const textIndex = new Map();
  for (const f of readdirSync(DIR).filter((x) => /^texts-.*\.private\.jsonl$/.test(x))) {
    for (const t of readJsonl(join(DIR, f))) textIndex.set(`${t.arm}:${t.original_sha}`, t);
  }

  let repairedJudges = 0;
  let repairedRewrites = 0;
  let stillBroken = 0;

  for (const shard of shards) {
    const path = join(DIR, shard);
    const rows = readJsonl(path);
    let changed = false;

    for (const row of rows) {
      const key = `${row.arm}:${row.original_sha}`;
      const stored = textIndex.get(key);
      if (!stored) { log(`WARN no stored text for ${key}`); continue; }

      // 1. Repair a failed rewrite, then judge the new text.
      if (row.rewrite_error && !row.rewrite_sha) {
        log(`${row.arm}/${row.source_class}/${row.original_sha}: re-running failed rewrite`);
        if (DRY) { repairedRewrites += 1; continue; }
        const rw = await patinaRewrite(stored.original, row.lang);
        if (rw.text) {
          row.rewrite_sha = sha(rw.text);
          row.rewrite_chars = rw.text.length;
          row.rewrite_error = null;
          row.gate_failed = rw.gate_failed ?? false;
          row.gate_reason = rw.gate_reason ?? null;
          row.topped_up = true;
          row.judges.rewrite = {};
          stored.rewritten = rw.text;
          for (const j of JUDGES) {
            const res = await run(j.cmd, j.args, { input: judgePrompt(rw.text, row.lang), timeout: JUDGE_TIMEOUT_MS });
            const parsed = parseJudge(res.stdout);
            row.judges.rewrite[j.id] = parsed ? { ...parsed, topped_up: true } : { error: res.error || 'unparseable', retries_exhausted: true };
          }
          appendFileSync(join(DIR, 'texts-TOPUP.private.jsonl'), JSON.stringify(stored) + '\n');
          repairedRewrites += 1;
          changed = true;
          log(`  rewrite repaired (${rw.text.length} chars)`);
        } else {
          stillBroken += 1;
          log(`  rewrite STILL FAILING: ${rw.error}`);
        }
        continue;
      }

      // 2. Repair individual missing judge ratings.
      for (const cond of ['original', 'rewrite']) {
        const text = cond === 'original' ? stored.original : stored.rewritten;
        if (!text || !row.judges[cond]) continue;
        for (const j of JUDGES) {
          if (!missing(row.judges[cond][j.id])) continue;
          log(`${row.arm}/${row.source_class}/${row.original_sha}: re-judging ${cond} with ${j.id}`);
          if (DRY) { repairedJudges += 1; continue; }
          const res = await run(j.cmd, j.args, { input: judgePrompt(text, row.lang), timeout: JUDGE_TIMEOUT_MS });
          const parsed = parseJudge(res.stdout);
          if (parsed) {
            row.judges[cond][j.id] = { ...parsed, topped_up: true };
            repairedJudges += 1;
            changed = true;
            log(`  repaired -> ${parsed.ai_likeness} (${parsed.authorship})`);
          } else {
            row.judges[cond][j.id] = { error: res.error || 'unparseable', retries_exhausted: true, topup_failed: true };
            stillBroken += 1;
            log(`  STILL FAILING: ${res.error || 'unparseable'}`);
          }
        }
      }
    }

    if (changed && !DRY) writeFileSync(path, rows.map((r) => JSON.stringify(r)).join('\n') + '\n');
  }

  log(`top-up complete: ${repairedJudges} judge cells, ${repairedRewrites} rewrites repaired; ${stillBroken} still broken${DRY ? ' (DRY RUN)' : ''}`);
}

main().catch((e) => { log(`FATAL ${e?.stack ?? e}`); process.exit(1); });
